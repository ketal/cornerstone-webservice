package com.github.ketal.cornerstone.webservice.model;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class WsError {

    @XmlElement(name="error")
    private String errorMessage;

    private String referenceId;

    public WsError() {
    }

    public WsError(String error) {
        this(error, null);
    }

    public WsError(String errorMessage, String referenceId) {
        super();
        this.errorMessage = errorMessage;
        this.referenceId = referenceId;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage) {
        this.errorMessage = errorMessage;
    }

    public String getReferenceId() {
        return referenceId;
    }

    public void setReferenceId(String referenceId) {
        this.referenceId = referenceId;
    }

    @Override
    public String toString() {
        return "WsError [errorMessage=" + errorMessage + ((referenceId != null) ? ", referenceId=" + referenceId : "") + "]";
    }
}
